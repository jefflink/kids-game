def main():
    # Deduplicate keeping last entry
    seen = {}
    for c in CHARS:
        seen[c[0]] = tuple(c)
    char_list = list(seen.values())

    print(f"Processing {len(char_list)} characters...")
    db = []
    failed = []

    for char, py, mn, hsk in char_list:
        data = fetch_medians(char)
        if data is None:
            print(f"  SKIP {char} — not found in hanzi-writer-data")
            failed.append(char)
            continue
        ss = analyze(data['medians'])
        db.append({'char':char,'py':py,'mn':mn,'hsk':hsk,'sc':data['sc'],'ss':ss})
        print(f"  OK {char} ({data['sc']} strokes, {sum(len(s) for s in ss)} segments)")

    print(f"\n{len(db)} OK, {len(failed)} skipped: {failed}")

    # Emit JS
    lines = ['const CHAR_DB=[']
    for e in db:
        ss = json.dumps(e['ss'], separators=(',',':'))
        c = json.dumps(e['char'], ensure_ascii=False)
        lines.append(f"  {{char:{c},py:{json.dumps(e['py'])},mn:{json.dumps(e['mn'])},hsk:{e['hsk']},sc:{e['sc']},ss:{ss}}},")
    lines.append('];')
    js = '\n'.join(lines)

    # Inject into HTML
    html_path = os.path.join(os.path.dirname(__file__), 'hanzi-engine.html')
    if not os.path.exists(html_path):
        print(f"ERROR: {html_path} not found. Run from the same directory as hanzi-engine.html")
        sys.exit(1)

    html = open(html_path).read()
    start = html.index('const CHAR_DB=[')
    end = html.index('\n];', start) + 3
    html2 = html[:start] + js + html[end:]
    open(html_path, 'w').write(html2)
    print(f"\nUpdated {html_path} ({len(html2)} bytes, {len(db)} characters)")
    print("Done! Reload hanzi-engine.html in your browser.")

if __name__ == '__main__':
    main()
