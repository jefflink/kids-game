# ── FETCH STROKE DATA FROM NPM ─────────────────────────────────────────────────
def fetch_medians(char):
    """Load stroke median paths from hanzi-writer-data npm package."""
    script = f"""
const d = require('hanzi-writer-data/{char}');
const m = d.medians.map(s => s.map(([x,y]) => [
  Math.round(x*100/1024),
  Math.round((900-y)*100/1024)
]));
console.log(JSON.stringify({{sc:m.length,medians:m}}));
"""
    result = subprocess.run(['node', '-e', script], capture_output=True, text=True, cwd='/home/claude')
    if result.returncode != 0:
        return None
    return json.loads(result.stdout)

# ── STROKE ANALYSIS ────────────────────────────────────────────────────────────
TAU = math.pi * 2

def norm_a(a):
    a = a % TAU
    return a if a >= 0 else a + TAU

def angle_diff(a, b):
    d = abs(norm_a(a) - norm_a(b))
    return d if d <= math.pi else TAU - d

def resample(pts, n=32):
    pts = [(float(p[0]), float(p[1])) for p in pts]
    if len(pts) < 2: return pts
    total = sum(math.hypot(pts[i][0]-pts[i-1][0], pts[i][1]-pts[i-1][1]) for i in range(1,len(pts)))
    if total == 0: return [pts[0]] * n
    step = total/(n-1); out=[pts[0]]; acc=0; prev=pts[0]
    for i in range(1, len(pts)):
        d = math.hypot(pts[i][0]-prev[0], pts[i][1]-prev[1])
        while acc+d >= step and len(out) < n:
            t = (step-acc)/d if d > 0 else 0
            p = (prev[0]+t*(pts[i][0]-prev[0]), prev[1]+t*(pts[i][1]-prev[1]))
            out.append(p); prev=p; d=math.hypot(pts[i][0]-prev[0],pts[i][1]-prev[1]); acc=0
        acc+=d; prev=pts[i]
    while len(out) < n: out.append(pts[-1])
    return out[:n]

def normalize_bb(strokes):
    strokes = [[(float(p[0]),float(p[1])) for p in s] for s in strokes]
    all_pts = [p for s in strokes for p in s]
    xs=[p[0] for p in all_pts]; ys=[p[1] for p in all_pts]
    x0,y0=min(xs),min(ys)
    sc=max(max(xs)-x0,max(ys)-y0) or 1
    return [[(( p[0]-x0)/sc,(p[1]-y0)/sc) for p in s] for s in strokes]

# Improved split: triggers on EITHER a sudden direction change (>35°)
# OR cumulative rotation since last split (>90°). Catches gradual curves like 子 stroke 1.
STEP_THRESH = math.pi * 0.20   # 36° single-step trigger
CUMUL_THRESH = math.pi * 0.55  # 99° cumulative rotation trigger

def split_inflections(pts):
    r = resample(pts, 32)
    if len(r) < 3: return [r]
    dirs = [math.atan2(r[i+1][1]-r[i][1], r[i+1][0]-r[i][0]) for i in range(len(r)-1)]
    splits = [0]
    cumul = 0.0
    last_dir = dirs[0]
    for i in range(1, len(dirs)):
        step = angle_diff(dirs[i], dirs[i-1])
        cumul += step
        if step > STEP_THRESH or cumul > CUMUL_THRESH:
            splits.append(i)
            cumul = 0.0
            last_dir = dirs[i]
    splits.append(len(r)-1)
    segs = []
    for i in range(len(splits)-1):
        seg = r[splits[i]:splits[i+1]+1]
        if len(seg) >= 2: segs.append(seg)
    return segs if segs else [r]

def seg_desc(pts):
    a=pts[0]; b=pts[-1]
    d=norm_a(math.atan2(b[1]-a[1],b[0]-a[0]))
    l=math.hypot(b[0]-a[0],b[1]-a[1])
    cx=sum(p[0] for p in pts)/len(pts)
    cy=sum(p[1] for p in pts)/len(pts)
    return [round(d,4),round(l,4),round(cx,4),round(cy,4)]

def analyze(strokes):
    norm = normalize_bb(strokes)
    return [[seg_desc(s) for s in split_inflections(stroke)] for stroke in norm]

